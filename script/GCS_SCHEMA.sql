CREATE FUNCTION [dbo].[obtener_estado_actual_seguimiento_elemento_congiguracion] 
(
	@D_SEGUIMIENTO int
)
RETURNS varchar(max)
AS
begin
	declare @estados varchar(max) = ''
	declare @estado varchar(max) = ''
	DECLARE estados_cursor CURSOR FOR
SELECT        dbo.ESTADO_ACTUAL_ELEMENTO_CONFIGURACION.NOMBRE_ESTADO_ACTUL
FROM            dbo.DETALLE_ESTADO_ACTUAL_ELEMENTO_CONFIGURACION INNER JOIN
                         dbo.ESTADO_ACTUAL_ELEMENTO_CONFIGURACION ON 
                         dbo.DETALLE_ESTADO_ACTUAL_ELEMENTO_CONFIGURACION.ID_ESTADO_ACTUAL = dbo.ESTADO_ACTUAL_ELEMENTO_CONFIGURACION.ID_ESTADO_ACTUAL
WHERE        (dbo.DETALLE_ESTADO_ACTUAL_ELEMENTO_CONFIGURACION.ID_SEGUIMIENTO = @D_SEGUIMIENTO)
	OPEN estados_cursor
	FETCH NEXT FROM estados_cursor
	INTO @estado
	set @estados=''
	WHILE @@FETCH_STATUS = 0
	BEGIN
		 SET @estados=@estados + @estado + ', '
		 FETCH NEXT FROM estados_cursor INTO @estado
	END
	CLOSE estados_cursor
	DEALLOCATE estados_cursor
	if len(@estados)>0
		begin
			set @estados = SUBSTRING(@estados,1,len(@estados)-1)
		end
	else
		begin
			set @estados = '------'
		end
	return @estados
END

