CREATE FUNCTION [dbo].[OBTENER_UBICACION_LICENCIA_AREA] 
(
@_ID_LICENCIA_ELE_CONF	VARCHAR(MAX)=null,
@_TIPO_LICENCIA_ELE_CONF	VARCHAR(MAX)=null,
@_NOMBRE_LICENCIA_ELE_CONF	VARCHAR(MAX)=null
)
RETURNS varchar(max)
AS
BEGIN
    declare @_AREA VARCHAR(MAX)
	SELECT       @_AREA = A.Descripcion_Area
              
FROM          dbo.Local AS L INNER JOIN
              dbo.Sede as S ON l.Id_Sede = S.Id_Sede INNER JOIN
                         dbo.Area AS A ON l.Id_Local = A.Id_Local INNER JOIN
                         dbo.LICENCIA_ELEMENTO_CONFIGURACION AS LEC INNER JOIN
                         dbo.TIPO_LICENCIA_ELEMENTO_CONFIGURACION AS TLEC ON 
                         LEC.ID_TIPO_LICENCIA_CI = TLEC.ID_TIPO_LICENCIA_CI INNER JOIN
                         dbo.RELACION_LICENCIA_ELEMENTO_CONFIGURACION AS RLEC ON 
                         LEC.ID_LICENCIA_ELE_CONF = RLEC.ID_LICENCIA_ELE_CONF INNER
                          JOIN
                         dbo.ELEMENTOS_CONFIGURACION AS EC ON 
                         RLEC.ID_ELEMENTO_CONFIGURACION = EC.ID_ELEMENTO_CONFIGURACION
                          ON A.Id_Area = EC.LOCALIZACION_FISICA
GROUP BY LEC.ID_LICENCIA_ELE_CONF, TLEC.NOMBRE_LICENCIA_CI, 
                         LEC.NOMBRE, A.Descripcion_Area
HAVING        (LEC.ID_LICENCIA_ELE_CONF = @_ID_LICENCIA_ELE_CONF) AND 
                         (TLEC.NOMBRE_LICENCIA_CI = @_TIPO_LICENCIA_ELE_CONF) AND 
                         (LEC.NOMBRE = @_NOMBRE_LICENCIA_ELE_CONF)
    return @_AREA
END


